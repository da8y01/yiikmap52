<p>El <i>K-Map</i> <b>(Knowledge Map / Mapa de Conocimiento)</b> es un recurso organizacional (específicamente un capital estructural de carácter intangible) que Gestiona el Conocimiento Organizacional interrelacionando las principales entidades actrices del modelo de conocimiento organizacional.</p>

<p>Al ser una herramienta de <i>KM</i> <b>(Knowledge Management / Gestión del Conocimiento)</b> pretende presentar soluciones a operaciones comunes de la organización presentando de una manera intuitiva y eficiente las probablemente más acertadas alternativas de conocimiento apropiado para las necesidades de la operación a gestionar.</p>

<p>Orientado a <i>BPM</i> <b>(Business Process Management / Gestión de Procesos de Negocio)</b> el <i>K-Map</i> como herramienta de <i>KM</i> organizacional proporciona información acertada acerca de qué conocimiento es concerniente a un proceso u otras entidades del modelo de conocimiento organizacional.</p>

